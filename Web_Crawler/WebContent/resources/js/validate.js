	validateUrl : function(url){
		if(url != null){
			return this.isURL(url);
		}else{
			return false;
		}
	},
	validateDepth : function(depth){
		if(depth != null){
			var numDepth = parseInt(depth);
			if(isNaN(numDepth)){
				return false;
			}
			if(numDepth <= 0){
				return false;
			}else{
				return true;
			}
		}else{
			return false;
		}
	},
